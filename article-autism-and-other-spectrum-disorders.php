				<p>Rett’s Disorder has only appeared in girls to date. These children seem to develop normally until between 5-18 months, then experience a deceleration of head growth and lose previously acquired language. Hand skills are replaced by stereotypical behaviour (hand-flapping, wringing). There is also a loss of social interaction, physical coordination and receptive and expressive language is impaired. Recently, a genetic marker for Rett’s syndrome has been identified.</p>
				
